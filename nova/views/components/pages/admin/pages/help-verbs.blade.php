<p>HTTP defines methods (often referred to as <em>verbs</em>) to indicate the desired action to be performed on a specific resource.</p>

<dl>
	<dt>GET</dt>
	<dd>Requests a representation of the specified resource. Requests using GET should only retrieve data and should have no other effect.</dd>

	<dt>POST</dt>
	<dd>Requests that the server accept the entity enclosed in the request as a new resource. This is most often used for creating new data on the server or in the database.</dd>

	<dt>PUT</dt>
	<dd>If the URI refers to an already existing resource, it's modified with the data passed through the request. This is most often used for updating existing pieces of data.</dd>

	<dt>DELETE</dt>
	<dd>Deletes the specified resource.</dd>
</dl>